#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		j;
	size_t	i;

	j = 0;
	i = ft_strlen(dest);
	while (src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
